from numpy import *
from matplotlib.pyplot import *
t=arange(0,10,0.001)
x=sin(t)
subplot(2,4,1)
x1=gradient(x)
plot(t,x1)
subplot(2,4,2)
x2=sinh(t)
plot(t,x2)
subplot(2,4,3)
x3=gradient(x2)
plot(t,x3)
y=cos(t)
subplot(2,4,4)
y1=gradient(y)

plot(t,y1)
subplot(2,4,5)
y2=cosh(t)
plot(t,y2)
subplot(2,4,6)
y3=gradient(y2)
plot(t,y3)
show()